// Thana coordinate offsets (approximate, relative to district center)
export const getThanaCoordinates = (districtName, thanaName) => {
  const base = districtCoordinates[districtName];
  if (!base) return [23.8103, 90.4125]; // Default to Dhaka

  // Generate slightly varied coordinates based on thana name hash
  const hash = thanaName.split('').reduce((acc, char) => acc + char.charCodeAt(0), 0);
  const latOffset = ((hash % 100) - 50) / 500;
  const lngOffset = ((hash % 73) - 36) / 500;

  return [base[0] + latOffset, base[1] + lngOffset];
};
