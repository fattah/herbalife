export const getAllDistricts = () => bangladeshData.map(d => d.district).sort();

export const getThanasForDistrict = (districtName) => {
  const found = bangladeshData.find(d => d.district === districtName);
  return found ? found.thanas.sort() : [];
};

export const getAllThanas = () => {
  const all = [];
  bangladeshData.forEach(d => {
    d.thanas.forEach(t => all.push({ thana: t, district: d.district }));
  });
  return all.sort((a, b) => a.thana.localeCompare(b.thana));
};

